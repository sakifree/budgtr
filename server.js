///////////////////////////////////
// SETUP - DECLARE DEPENDENCIES - CREATE APP OBJECT
///////////////////////////////////
require("dotenv").config()
const express = require('express')
const app = express()
const morgan = require("morgan")

///////////////////////////////////
// MIDDLEWARE
///////////////////////////////////
app.use(morgan("dev"))

///////////////////////////////////
// ROUTES 
///////////////////////////////////

///////////////////////////////////
// SERVER LISTENER
///////////////////////////////////
const PORT = process.env.PORT || 3000
app.listen(PORT, () => {
    console.log(`Listening on planet ${PORT}`)
})